def _cl2_fired(case: dict) -> bool:
    """Return True if HRW-CL2 (ix_hrw_cl2_convex_sna_retrusive) fired for this case."""
    stage_e = (case.get("stage_info") or {}).get("stage_E_hrw") or {}
    flag = stage_e.get("hrw_clinical_flag") or {}
    violations = flag.get("violations") or []
    return any(v.get("rule_id") == _CL2_RULE_ID for v in violations)


def _falsification_incomplete(case: dict) -> bool:
    """Return True if v3 §13 direction_falsification gate fired for this case."""
    stage_e = (case.get("stage_info") or {}).get("stage_E_hrw") or {}
    flag = stage_e.get("hrw_clinical_flag") or {}
    violations = flag.get("violations") or []
    return any(v.get("rule_id") == _DF_GATE_RULE_ID for v in violations)


def _df_gate_should_suppress_s4(content: str, voice_mode: str) -> bool:
    """Mode-conditional + clause-level lexeme guard for s4_行动建议 when DF gate fired.

    Mode A / unknown → always suppress (firm direction without falsification = dangerous).
    Mode B → suppress only if s4 contains a firm-direction lexeme OUTSIDE a conditional bracket.
    DW Mode B invariant (SC L550): Mode B s4 must use bracketed conditionals for any direction.
    76db discipline: don't trust SC self-classification alone — keep thin backstop for mislabel.
    """
    if voice_mode != "B_difficult_diagnosis_warning":
        return True  # Mode A or unknown → suppress (safe default)
    for clause in re.split(r'[。；\n]', content or ""):
        clause = clause.strip()
        if clause and _DF_FIRM_LEXEMES.search(clause):
            if not _DF_CONDITIONAL_MARKERS.search(clause):
                # Firm imperative outside conditional bracket → SC mislabel detected → suppress
                return True
    return False  # All firm lexemes in conditional brackets (W8 compliant) → keep


def _v4_review_required(case: dict, final_output: Optional[dict] = None) -> bool:
    """V4 Phase I frontend surface: review-required gates must be visible, not metadata-only."""
    final_output = final_output or {}
    prompt = final_output.get("v4_review_prompt") or {}
    if prompt.get("visible"):
        return True
    meta = case.get("metadata") or {}
    v4 = meta.get("v4_phase1") or {}
    gate = v4.get("v4_gate_packet") or {}
    gate_result = gate.get("gate_result")
    sc_status = v4.get("v4_sc_gate_consistency") or {}
    if gate_result and gate_result != "true_convex_closed":
        return True
    if sc_status.get("consistent") is False:
        return True
    if sc_status.get("anchor_dispute_present"):
        return True
    return False


def _v4_phase1_packet(case: dict, final_output: Optional[dict] = None) -> dict:
    final_output = final_output or {}
    phase1 = final_output.get("v4_phase1")
    if isinstance(phase1, dict) and phase1:
        return phase1
    meta = case.get("metadata") or {}
    phase1 = meta.get("v4_phase1")
    return phase1 if isinstance(phase1, dict) else {}


