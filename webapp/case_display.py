def _to_cst(utc_str: str) -> str:
    try:
        dt = datetime.fromisoformat(utc_str).replace(tzinfo=timezone.utc)
        return dt.astimezone(_CST).strftime("%Y-%m-%d %H:%M CST")
    except Exception:
        return utc_str[:16].replace("T", " ")

