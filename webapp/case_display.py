def _text_values(value) -> list[str]:
    if isinstance(value, str):
        return [value]
    if isinstance(value, dict):
        texts = []
        for item in value.values():
            texts.extend(_text_values(item))
        return texts
    if isinstance(value, (list, tuple)):
        texts = []
        for item in value:
            texts.extend(_text_values(item))
        return texts
    return []


def _meaningful_signal(value) -> bool:
    if value is None:
        return False
    if isinstance(value, str):
        return bool(value.strip()) and not _EMPTY_SIGNAL_RE.match(value)
    if isinstance(value, (list, tuple)):
        return any(_meaningful_signal(item) for item in value)
    if isinstance(value, dict):
        return any(_meaningful_signal(item) for item in value.values())
    return bool(value)


def _explicit_school_compare_requested(submission: dict) -> bool:
    request_fields = {
        "chief_complaint_patient": submission.get("chief_complaint_patient"),
        "chief_complaint_doctor": submission.get("chief_complaint_doctor"),
        "chief_complaint_main_concern": submission.get("chief_complaint_main_concern"),
        "doctor_specific_question": submission.get("doctor_specific_question"),
        "context_notes": submission.get("context_notes"),
    }
    return any(_SCHOOL_COMPARE_REQUEST_RE.search(text) for text in _text_values(request_fields))


def _joint_boundary_relevant(submission: dict) -> bool:
    findings = submission.get("clinical_findings") or {}
    if isinstance(findings, dict):
        tmd = findings.get("tmd_signals") or {}
        if isinstance(tmd, dict):
            structured_fields = (
                tmd.get("active_symptoms"),
                tmd.get("symptom_progression"),
                tmd.get("cbct_findings"),
                tmd.get("mri_findings"),
            )
            if any(_meaningful_signal(field) for field in structured_fields):
                return True

    boundary_fields = {
        "chief_complaint_patient": submission.get("chief_complaint_patient"),
        "chief_complaint_doctor": submission.get("chief_complaint_doctor"),
        "chief_complaint_main_concern": submission.get("chief_complaint_main_concern"),
        "doctor_specific_question": submission.get("doctor_specific_question"),
        "context_notes": submission.get("context_notes"),
    }
    for text in _text_values(boundary_fields):
        if _JOINT_BOUNDARY_RE.search(text) and not _JOINT_NEGATIVE_RE.search(text):
            return True
    return False


def _should_render_school_section(case: dict, final_output: Optional[dict] = None) -> bool:
    submission = case.get("submission") or {}
    return _explicit_school_compare_requested(submission) or _joint_boundary_relevant(submission)


def _has_school_section(sections: dict) -> bool:
    return isinstance(sections, dict) and any(key in _SCHOOL_SECTION_KEYS for key in sections)


def _strip_school_markdown_section(markdown: str) -> str:
    if not markdown:
        return markdown
    pattern = re.compile(
        r"(?ms)^#{1,4}\s*三[、.．]\s*(?:3-school|三学派|学派|框架).*?"
        r"(?=^#{1,4}\s*(?:四[、.．]|4[、.．])|\Z)"
    )
    return pattern.sub("", markdown).strip()


